package ctu.edu.vn.dlab.model;

public class RegistrationLabStudent {
	private String idLab;
	private String name;
	private String amount;
	private String totalRegisted;
	private String idCourse;
	private String idTeacher;
	private String idUser;
	private String fullName;

	public String getIdLab() {
		return idLab;
	}

	public void setIdLab(String idLab) {
		this.idLab = idLab;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getTotalRegisted() {
		return totalRegisted;
	}

	public void setTotalRegisted(String totalRegisted) {
		this.totalRegisted = totalRegisted;
	}

	public String getIdCourse() {
		return idCourse;
	}

	public void setIdCourse(String idCourse) {
		this.idCourse = idCourse;
	}

	public String getIdTeacher() {
		return idTeacher;
	}

	public void setIdTeacher(String idTeacher) {
		this.idTeacher = idTeacher;
	}

	public String getIdUser() {
		return idUser;
	}

	public void setIdUser(String idUser) {
		this.idUser = idUser;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

}
