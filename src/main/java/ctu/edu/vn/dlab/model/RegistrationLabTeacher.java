package ctu.edu.vn.dlab.model;

public class RegistrationLabTeacher {
	private String idLab;
	private String idStudent;
	private String idStudent_Class;
	private String idUser;
	private String fullName;
	private String email;

	public RegistrationLabTeacher() {
		super();
	}

	public String getIdLab() {
		return idLab;
	}

	public void setIdLab(String idLab) {
		this.idLab = idLab;
	}

	public String getIdStudent() {
		return idStudent;
	}

	public void setIdStudent(String idStudent) {
		this.idStudent = idStudent;
	}

	public String getIdStudent_Class() {
		return idStudent_Class;
	}

	public void setIdStudent_Class(String idStudent_Class) {
		this.idStudent_Class = idStudent_Class;
	}

	public String getIdUser() {
		return idUser;
	}

	public void setIdUser(String idUser) {
		this.idUser = idUser;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

}
